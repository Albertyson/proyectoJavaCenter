    private void btnEnviarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEnviarMouseClicked
        // TODO add your handling code here:
        try {
            ExceptionVacios();
            ExceptionArroba();
            EnviadorMail EnviadorMail = new EnviadorMail(txtDestino.getText(), txtAsunto.getText(), txtMensaje.getText());
            JOptionPane.showMessageDialog(null, "Mensaje Enviado");
            FileWriter fw = null;
            BufferedWriter bw = null;
            File mensaje = new File("C:/" + usr + "/Correo/" + txtAsunto.getText() + ".txt");
            try {
                fw = new FileWriter(mensaje.getPath(), true);
                bw = new BufferedWriter(fw);
                String text = txtMensaje.getText();
                int totalLines = txtMensaje.getLineCount();
                bw.write("Para: " + txtDestino.getText());
                bw.newLine();
                bw.flush();
                for (int i = 0; i < totalLines; i++) {
                    int inicio = txtMensaje.getLineStartOffset(i);
                    int fin = txtMensaje.getLineEndOffset(i);
                    String linea = text.substring(inicio, fin);
                    bw.write(linea);
                    bw.newLine();
                    bw.flush();
                }
                bw.flush();
                mensaje.createNewFile();
            } catch (Exception e) {
            } finally {
                try {
                    fw.close();
                } catch (IOException ex) {
                }
            }
            txtMensaje.setText("");
            txtDestino.setText("");
            txtAsunto.setText("");
        } catch (Exceptions ex) {
            JOptionPane.showMessageDialog(this.getFrames()[0], ex.getMessage());
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnEnviarMouseClicked

